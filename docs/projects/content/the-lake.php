    </header>
    <section class="space-y-2 text-sm">
      <p>The Lake is the first in a series of works that track natural biological motion via electronic tagging systems and transforms the data collected into musical composition and animation. Supported by Nesta and first presented at Tingrith Fishery, Bedfordshire, UK, the work collected real-time movement data from sixteen fish and used a custom developed software and hardware system to create a site-specific experience.</p>
      <p> It is designed to illustrate a hidden world of fish movement and present it as an artistic performance by using technology; creating an interface between biological movements and behaviours and technological systems in an artistic context, posing the question - can the rhythmic beauty of a fish movement be translated into aural composition? Can fish make music?</p>
